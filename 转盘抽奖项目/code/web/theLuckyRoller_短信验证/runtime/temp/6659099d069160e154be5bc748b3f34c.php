                            var promise = new Promise((resolve, reject) => {
                                var setTimer = setInterval(
                                    () => {
                                        seconds -= 1;
                                        that.html(seconds+'秒');
                                        if (seconds <= 0) {
                                            that.html('获取验证码');
                                            resolve(setTimer)
                                        }
                                    }
                                    , 1000)
                            });
                            promise.then((setTimer) => {
                                clearInterval(setTimer);
                                that.attr("disabled", false);
                            })
                        }
                        message(res,str)
