            function checkForm(){
                var str,
                isMobile=/^1[3-9]\d{9}$/,
                isPhone=/^((0\d{2,3})-)?(\d{7,8})(-(\d{3,}))?$/,
                reg = /^\d{4}$/;

                if ($('#realname').val() == '') {
                    str = {code: 2001, errorMsg: "请问怎么称呼您？"}
                    //$('#realname').focus();
                } else if ($('#realname').val().length < 2) {
                    str = {code: 2001, errorMsg: "你的名字不得小于2个字符！"}
                } else if ($('#realname').val().length > 20) {
                    str = {code: 2001, errorMsg: "你的名字不得大于20个字符！"}
                } else if ($('#tel').val() == '') {
                    str = {code: 2001, errorMsg: "请填写电话号码，以便我们与你取得联系！"}
                } else if (!isMobile.test($('#tel').val()) && !isPhone.test($('#tel').val())) {
                    str = {code: 2001, errorMsg: "请正确填写电话号码，例如:134xxxx1234或021-4815xxx"}
                } else if ($('#code').val() == '') {
                    str = {code: 2001, errorMsg: "输入验证码！"}
                } else if (!reg.test($('#code').val())) {
                    str = {code: 2001, errorMsg: "请填写正确的四位数字验证码"}
                }
                if (str) {
                    message(str);
                    return false;
                } else {
                    return true;
                }

            }
